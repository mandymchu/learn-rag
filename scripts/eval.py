# This script evaluates the RAG implementation by asking specific questions about the codebase and checking if the retrieved sources are correct.

# Expected output format:
# [PASS] Where is cosine similarity implemented? → embeddings.py ✓
# [PASS] How are documents loaded from disk? → chunker.py ✓
# ...
# 5/5 passed

import sys
import os
sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", "src"))

from learn_rag.rag_pipeline import rag_ask
from learn_rag.vector_store import VectorStore

TEST_CASES = [
    {
        "question": "Where is cosine similarity implemented?",
        "expected_sources": ["embeddings.py"],
    },
    {
        "question": "How are documents loaded from disk?",
        "expected_sources": ["chunker.py"],
    },
    {
        "question": "What is the prompt format used to call the LLM?",
        "expected_sources": ["rag_pipeline.py"],
    },
    {
        "question": "What are the file types supported for chunking?",
        "expected_sources": ["chunker.py"],
    },
    {
        "question": "Where is the basic configuration for the OpenAI API client set up?",
        "expected_sources": ["config.py"],
    },
]

if __name__ == "__main__":
    passed = 0
    # vectore_store.json is generated by loading and chunking the .py scripts in src/learn_rag
    vector_store_path = os.path.join(os.path.join(os.path.dirname(__file__), "test_results", "vector_store.json"))
    vector_store = VectorStore()
    vector_store.load(vector_store_path)
    for test in TEST_CASES:
        question = test["question"]
        expected_sources = test["expected_sources"]
        answer, sources = rag_ask(question, top_k=5, loaded_vector_store=vector_store)
        print(f"Question: {question}\n")
        print(f"Answer: {answer}\n")
        print("Sources:")
        for source in sources:
            print(f"- {source}")
        retrieved_files = [source.split(" (")[0] for source in sources]  # extract filename from source info
        if all(any(expected in retrieved for retrieved in retrieved_files) for expected in expected_sources):
            print(f"[PASS] {question} → {', '.join(expected_sources)} ✓\n")
            passed += 1
        else:
            print(f"[FAIL] {question} → expected: {', '.join(expected_sources)}, got: {', '.join(retrieved_files)}")
    print(f"{passed}/{len(TEST_CASES)} passed")
